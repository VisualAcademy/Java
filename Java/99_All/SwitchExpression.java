// switch 문: 선택문
public class SwitchExpression {
    public static void main(String[] args) {
        int x = 2;

        switch (x) {
            case 1:
                System.out.println("1입니다."); // x가 1일 때 실행
                break;
            case 2:
                System.out.println("2입니다."); // x가 2일 때 실행
                break;                
        }
    }
}
