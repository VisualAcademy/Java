package com.devlec;

public class Main {
    public static void main(String[] args) {
        int number1 = 1234; System.out.println(number1); // byte, short, int, long

        double number2 = 3.14d; System.out.println(number2); // float, double

        String message = "안녕하세요."; System.out.println(message);

        boolean bln = true; System.out.println(bln); // true, false

        Object all = 3.14f; System.out.println(all.toString()); // "모든 값"
    }
}
