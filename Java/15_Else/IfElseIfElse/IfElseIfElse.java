public class IfElseIfElse {
    public static void main(String[] args) {
        int number1 = 10;
        int number2 = 20;

        if (number1 > number2) {
            System.out.println("number1이 더 크다.");
        }
        else if (number1 < number2) {
            System.out.println("number2가 더 크다.");
        }
        else {
            System.out.println("둘 다 같다.");            
        }
    }
}
