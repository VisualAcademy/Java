public class StringCompareValue {
    public static void main(String[] args) {
        String s1 = "JavaCampus";
        String s2 = "javacampus";

        if (s1 == s2) {
            System.out.println("같다.");
        }
        else {
            System.out.println("다르다.");
        }
    }
}
